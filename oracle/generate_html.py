def render_gematria_section(text):
    lines = text.splitlines()
    in_block = False
    rendered = []
    for line in lines:
        if "‚ú° GEMATRIA SYNCHRONICITY ‚ú°" in line:
            in_block = True
            rendered.append("<h2 class='gematria'>‚ú° Gematria Synchronicity ‚ú°</h2>")
            continue
        if in_block:
            if line.strip().startswith("==") or line.strip() == "":
                in_block = False
                rendered.append("<hr class='divider'>")
            else:
                rendered.append(f"<pre class='gem-line'>{escape(line)}</pre>")
        else:
            rendered.append(f"<pre>{escape(line)}</pre>")
    return "\n".join(rendered)

def create_page(filename):
    archive_path = os.path.join(archive_dir, filename)
    output_filename = filename.replace(".txt", ".html")

    with open(archive_path, "r", encoding="utf-8") as src:
        content = src.read()

    html_content = render_gematria_section(content)

    title = filename.replace(".txt", "")

    # Load the full content
    with open(archive_path, "r", encoding="utf-8") as src:
        content = src.read()

    # Try to extract a slug from the Oracle message (if it exists)
    oracle_line = ""
    for line in content.splitlines():
        if line.strip().startswith("2. Oracle Prophecy:"):
            idx = content.splitlines().index(line)
            try:
                oracle_line = content.splitlines()[idx + 1].strip()
            except IndexError:
                oracle_line = "prophecy"
            break

    # Fallback if we don't get a clean line
    if not oracle_line:
        oracle_line = "prophecy"

    # Clean it: lowercase, dashes, alphanumeric only
    import re
    slug = re.sub(r'[^a-z0-9]+', '-', oracle_line.lower())[:93].strip("-")

    output_filename = f"{title}-{slug}.html"
    output_path = os.path.join(output_dir, output_filename)

    # Try parsing with timestamp format, fallback to date only
    try:
        date_str = datetime.strptime(title, "%Y-%m-%d-%H%M%S").strftime("%B %d, %Y ‚Äî %H:%M")
    except ValueError:
        try:
            date_str = datetime.strptime(title, "%Y-%m-%d").strftime("%B %d, %Y")
        except ValueError:
            date_str = title  # fallback to raw title if all else fails

    page_html = PAGE_TEMPLATE.format(title="FUCKUP¬≤ Prophecy", date=date_str, content=html_content)

    with open(output_path, "w", encoding="utf-8") as f:
        f.write(page_html)

    print(f"‚úÖ Generated page: {output_filename}")
    return output_filename

